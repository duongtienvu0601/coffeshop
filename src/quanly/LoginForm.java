    private void login() {
        
        String manv = txttk.getText();
        String matKhau = new String(txtmk.getPassword());
        try {
            NhanVien User = dao.findById(manv);
            if (User != null) {
                String matKhau2 = User.getPassword();
                if (matKhau.equals(matKhau2)) {
                    ShareHelper.USER = User;
                    MainJFrame nv = new MainJFrame();
                    nv.setVisible(true);
                    this.dispose();
                } else {
                    alert("Sai mật khẩu!");
                }
            } else {
                alert("Tài khoản này không tồn tại");
                //JOptionPane.showMessageDialog(this,"ktt");
            }
        } catch (Exception e) {
            alert("Lỗi truy vấn dữ liệu!");
        }
    }

